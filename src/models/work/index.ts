export enum Genre {
  Code = "code",
  Design = "design",
  Robotics = "robotics",
  Academic = "academic",
  Others = "others",
}

export default class Work {
  #date: string;
  #title: string;
  #body: string;
  #genre: Genre[];
  #media: string[];

  get date(): string {
    return this.#date;
  }
  get title(): string {
    return this.#title;
  }
  get body(): string {
    return this.#body;
  }
  get genre(): Genre[] {
    return this.#genre;
  }
  get media(): String[] {
    return this.#media;
  }

  constructor(
    date: string,
    title: string,
    body: string,
    genre: Genre[],
    media: string[]
  ) {
    this.#date = date;
    this.#title = title;
    this.#body = body;
    this.#genre = genre;
    this.#media = media;
  }
}